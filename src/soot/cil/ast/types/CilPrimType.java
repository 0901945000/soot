package soot.cil.ast.types;

/**
 * Enumeration containing all the primitive types available in CIL
 * 
 * @author Steven Arzt
 *
 */
public enum CilPrimType {
	
	cilVoid,
	cilChar,
	cilString,
	cilObject,
	cilBool,
	
	cilInt,
	cilInt8,
	cilInt16,
	cilInt32,
	
	cilFloat32,
	cilFloat64,
	
	cilUInt,
	cilUInt8,
	cilUInt16,
	cilUInt32,
	cilUInt64,
	
	cilDecimal,

}
